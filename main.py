# Функция, которая находит минимальное количество купюр, необходимых для выплаты указанной суммы
def min_num_bills(n: int) -> dict:
    # Денежные купюры, которые мы будем использовать
    bills = [64, 32, 16, 8, 4, 2, 1]
    # Словарь, где будем хранить количество купюр каждого достоинства
    num_bills = {}
    for bill in bills:
        # Считаем, сколько купюр данного достоинства нужно для выплаты суммы
        num = n // bill
        # Если купюр нужно больше нуля, добавляем их в словарь
        if num > 0:
            num_bills[bill] = num
        # Вычитаем выплаченную сумму из n
        n -= num * bill
    # Возвращаем словарь с количеством купюр каждого достоинства
    return num_bills

# Спрашиваем у пользователя сумму, которую нужно выплатить
n = int(input("Введите сумму выплаты: "))

# Вызываем функцию и выводим результат
print(min_num_bills(n))